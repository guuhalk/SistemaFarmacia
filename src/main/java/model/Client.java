package model;

public class Client {
	private Integer idClient;
	private String nameClient;
	private String addressClient;
	private String cpfClient;
	private Integer statusClient;
	private String emailClient;
	private String numberClient;

	// Getters and Setters
	public Integer getIdClient() {
		return idClient;
	}

	public void setIdClient(Integer idClient) {
		this.idClient = idClient;
	}

	public String getNameClient() {
		return nameClient;
	}

	public void setNameClient(String nameClient) {
		this.nameClient = nameClient;
	}

	public String getAddressClient() {
		return addressClient;
	}

	public void setAddressClient(String addressClient) {
		this.addressClient = addressClient;
	}

	public String getCpfClient() {
		return cpfClient;
	}

	public void setCpfClient(String cpfClient) {
		this.cpfClient = cpfClient;
	}

	public Integer getStatusClient() {
		return statusClient;
	}

	public void setStatusClient(Integer statusClient) {
		this.statusClient = statusClient;
	}

	public String getEmailClient() {
		return emailClient;
	}

	public void setEmailClient(String emailClient) {
		this.emailClient = emailClient;
	}

	public String getNumberClient() {
		return numberClient;
	}

	public void setNumberClient(String numberClient) {
		this.numberClient = numberClient;
	}

}
